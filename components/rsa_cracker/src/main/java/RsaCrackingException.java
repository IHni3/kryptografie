public class RsaCrackingException extends Exception {

    public RsaCrackingException() {
        super();
    }

    public RsaCrackingException(String errorMessage) {
        super(errorMessage);
    }

    public RsaCrackingException(Throwable throwable) {
        super(throwable);
    }

    public RsaCrackingException(String errorMessage, Throwable throwable) {
        super(errorMessage, throwable);
    }
}
